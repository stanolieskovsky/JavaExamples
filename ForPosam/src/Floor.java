public class Floor {

    public Floor() {
    }
   private int count; 
   private String name;
   private int sizeM;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
   
   
    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getSizeM() {
        return sizeM;
    }

    public void setSizeM(int sizeM) {
        this.sizeM = sizeM;
    }
    
   
}
